public class Variavel1 {
	
	public static void main(String[] args){
		int quantidade; //declarendo a variável inteira
		quantidade = 10;//dando valor a varíavel "quantidade"
		System.out.println(quantidade);
		quantidade = 15;//A quantidade pode ser alterada , desde que mostre depois
		System.out.println(quantidade);
	}
}
